#define GENERATORDENSE_HPP

#include <string>

namespace libxsmm {

  /**
   * This class generates a dense matrix multiply kernel
   */
  class GeneratorDense {
    private:
      bool bAlignedA_;
      bool bAlignedC_;
      std::string tVec_;
      std::string tPrefetch_;
      bool bSP_;

    public:
      GeneratorDense();

      GeneratorDense(bool bAlignedA, bool bAlignedC, std::string tVec, std::string tPrefetch, bool SP);

      ~GeneratorDense();

      std::string generate_dense(bool bIsColMajor, int M, int N, int K, int lda, int ldb, int ldc, int i_alpha, int i_beta);
  };

}

#endif /* GENERATORDENSE_HPP */

